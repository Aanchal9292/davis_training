# function to calculate addition of two numbers
def addnumbers(x,y):
    return (x+y)
# -----------------------
# function to calculate subraction of two numbers
def diffnumbers(x,y):
    return (x-y)
# -----------------------
# function to calculate multiplication of two numbers
def productnumbers(x,y):
    return (x*y)
# -----------------------
# function to calculate division of two numbers
def dividenumbers(x,y):
    return (x/y)
# -----------------------
# function to find remainder 
def calculatemodulo(x,y):
    return (x%y)
